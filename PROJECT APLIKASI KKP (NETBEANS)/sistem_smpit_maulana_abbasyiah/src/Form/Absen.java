    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "UPDATE absen set nama_siswa=?, kelas=?, hadir=?, tidak_hadir=?, izin=?, sakit=? WHERE nis=?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tNamaSiswa.getText());
            stat.setString(2, tKelas.getText());
            stat.setString(3, tHadir.getText());
            stat.setString(4, tTdkHadir.getText());
            stat.setString(5, tIzin.getText());
            stat.setString(6, tSakit.getText());
            stat.setString(7, CbRelasiNis.getSelectedItem().toString());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil diUbah ");
            kosong();
            tHadir.requestFocus();
            datatable();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal DiUbah " + e);
        }
    }//GEN-LAST:event_bEditActionPerformed

