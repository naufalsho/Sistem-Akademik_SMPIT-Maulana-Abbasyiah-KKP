    protected void datatable(){
        Object[] Baris = {
            "NIS",
            "Nama Siswa",
            "Kelas",
            "Hadir",
            "Tidak Hadir",
            "Izin",
            "Sakit"
        };
        
        tabmode = new DefaultTableModel(null, Baris);
        tableAbsen.setModel(tabmode);

        String sql = "SELECT * FROM absen";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("nis");
                String b = hasil.getString("nama_siswa");
                String c = hasil.getString("kelas");
                String d = hasil.getString("hadir");
                String e = hasil.getString("tidak_hadir");
                String f = hasil.getString("izin");
                String g = hasil.getString("sakit");
                
                String[] data={a, b, c, d, e, f, g};
                tabmode.addRow(data);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    protected void getRelasi(){
     
        String sql = "SELECT DISTINCT siswa.nis FROM siswa left join absen On siswa.nis = absen.nis;";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("siswa.nis");
                
                String[] data={a};
                CbRelasiNis.addItem(a);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
