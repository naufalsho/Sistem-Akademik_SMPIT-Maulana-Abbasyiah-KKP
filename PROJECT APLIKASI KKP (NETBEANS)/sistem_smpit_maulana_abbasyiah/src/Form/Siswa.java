    protected void datatable(){
        Object[] Baris = {
            "NIS",
            "Nama",
            "Tempat Lahir",
            "Tanggal Lahir",
            "Kelas",
            "Agama",
            "Jenis Kelamin",
            "Nama Wali",
            "Jenis Kelamin Wali",
            "No. Telp Wali",
            "Alamat Wali"
        };
        tabmode = new DefaultTableModel(null, Baris);
        tableSiswa.setModel(tabmode);

        String sql = "SELECT * FROM siswa";
        
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("nis");
                String b = hasil.getString("nama");
                String c = hasil.getString("tmp_lahir");
                String d = hasil.getString("tgl_lahir");
                String e = hasil.getString("kelas");
                String f = hasil.getString("agama");
                String g = hasil.getString("jenis_kel");
                String h = hasil.getString("nama_wali");
                String i = hasil.getString("jenis_kel_wali");
                String j = hasil.getString("no_telp_wali");
                String k = hasil.getString("alamat_wali");
                
                String[] data={a, b, c, d, e, f, g, h, i, j, k};
                tabmode.addRow(data);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
