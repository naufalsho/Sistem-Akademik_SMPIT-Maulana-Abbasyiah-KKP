    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO siswa VALUES (?,?,?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);

            stat.setString(1, tNis.getText());
            stat.setString(2, tNamaSiswa.getText());
            stat.setString(3, tTmpLahir.getText());
            stat.setString(4, tTglLahir.getText());
            stat.setString(5, cbRelasiKelas.getSelectedItem().toString());
            stat.setString(6, tAgama.getText());

            String jkel = "";
            if(rbLaki.isSelected()){
                jkel="Laki-Laki";
            }else{
                jkel="Perempuan";
            }

            stat.setString(7, jkel);
            stat.setString(8, tNamaWali.getText());
            
            String jkelWali = "";
            if(rbLakiWali.isSelected()){
                jkelWali="Laki-Laki";
            }else{
                jkelWali="Perempuan";
            }
            
            stat.setString(9, jkelWali);
            stat.setString(10, tNoTelpWali.getText());
            stat.setString(11, tAlamatWali.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "DATA Berhasil Disimpan");
            kosong();
            tNis.requestFocus();
            datatable();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }
    }//GEN-LAST:event_bSimpanActionPerformed

